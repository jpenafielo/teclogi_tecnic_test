package com.teclogi.app.Models;

public class Position {
    
    private Double x;

    private Double y;

    public void setX(Double x){
        this.x = x;
    }

    public void setY(Double y){
        this.y = y;
    }

    public Double getX(){
        return this.x;
    }

    public Double getY(){
        return this.y;
    }
}
